package operators;

public class IncrementOperators {
	
	public static void main(String arg[]) {
		
		//Adding 1 to an Integer
		
		int variable = 1;
		
		//By using Addition operator
		
		variable = variable +1;
	
		
		//By Using AutoIncrement operator
		
		 ++variable ; //Always add one
		 
		 //By using Compund Assignment operator
		 
		 variable+=1; // Can add any value
	}

}
